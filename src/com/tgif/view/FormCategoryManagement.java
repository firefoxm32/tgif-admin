    private void getMenus(String param) {
        TableManager.getTableModel(jXTableSuppliers).setRowCount(0);
        CategoryDao categoryDao = new CategoryDao();
        for (Category category : categoryDao.getMenusData(param)) {
            TableManager.getTableModel(jXTableSuppliers).addRow(new Object[]{
                category.getId(),
                category.getName(),
                category.getItemsCount()
            });
        }
    }

