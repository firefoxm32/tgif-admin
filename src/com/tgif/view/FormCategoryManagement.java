    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        if (jXTableSuppliers.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this, "Select record to edit");
            return;
        }
        
        Category category = new Category();
        String label = TableManager.getTableModel(jXTableSuppliers).getValueAt(jXTableSuppliers.getSelectedRow(), 1).toString();
        category.setName(label);
        int id = Integer.valueOf(TableManager.getTableModel(jXTableSuppliers).getValueAt(jXTableSuppliers.getSelectedRow(), 0).toString());
        category.setId(id);
        
        FormCategory form = new FormCategory(Main.JFParent, true);
        form.setTitle("Edit Category");
        form.setLocationRelativeTo(Main.JFParent);
        form.setCategory(category);
        form.setData();
        form.setVisible(true);
        jButtonSearch.doClick();
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        if (jXTableSuppliers.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this, "Select record to edit");
            return;
        }
        int id = Integer.valueOf(TableManager.getTableModel(jXTableSuppliers).getValueAt(jXTableSuppliers.getSelectedRow(), 0).toString());
        Category category = new Category();
        String label = TableManager.getTableModel(jXTableSuppliers).getValueAt(jXTableSuppliers.getSelectedRow(), 1).toString();
        category.setName(label);
        category.setId(id);
        CategoryDao categoryDao = new CategoryDao();
        categoryDao.save(category, "delete");
        jButtonSearch.doClick();
    }//GEN-LAST:event_jButtonDeleteActionPerformed

